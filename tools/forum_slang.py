def count(term):
    """Count occurrences of term in posts + comments."""
    like = f'%{term}%'
    p = db.execute("SELECT COUNT(*) FROM posts WHERE content LIKE ?", (like,)).fetchone()[0]
    c = db.execute("SELECT COUNT(*) FROM comments WHERE detail LIKE ?", (like,)).fetchone()[0]
    return p + c

